/**
 * Returns an array of cell co-ordinates immediately adjacent to the given cell.
 *
 * @param {Number[]} cell
 * @returns {Number[]}
 */
function neighbours(cell) {
    const [x, y] = cell;
    return [
        [x, y + 1],
        [x + 1, y + 1],
        [x + 1, y],
        [x + 1, y - 1],
        [x, y - 1],
        [x - 1, y - 1],
        [x - 1, y],
        [x - 1, y + 1]
    ];
}

/**
 * Returns a boolean value indicating whether a given cell is present in a set
 * of cells.
 *
 * @param {Number[]} cell
 * @param {Number[][]} cells
 * @returns Boolean
 */
function contains(cell, cells) {
    return cells.find(([x, y]) => x === cell[0] && y === cell[1]) !== undefined;
}

/**
 * Returns a boolean value indicating whether or not two cells are equivalent.
 *
 * @param {Number[]} cell
 * @param {Number[]} otherCell
 * @returns Boolean
 */
function equals(cell, otherCell) {
    return cell[0] === otherCell[0] && cell[1] === otherCell[1];
}

/**
 * Returns an integer representing the number of times a given cell value occurs
 * in a group of cells.
 *
 * @param {Number[]} cell
 * @param {Number[][]} cells
 * @returns {Number}
 */
function count(cell, cells) {
    return cells.reduce((counter, c) => counter + (equals(cell, c) ? 1 : 0), 0);
}

/**
 * Returns an array of cells which each occur exactly three times in a given
 * group of cells.
 *
 * @param {Number[][]} deadNeighbours
 * @returns {Number[][]}
 */
function resurrectees(deadNeighbours) {
    const resurrectees = [];
    for (const cell of deadNeighbours) {
        if (
            count(cell, deadNeighbours) === 3 &&
            !contains(cell, resurrectees)
        ) {
            resurrectees.push(cell);
        }
    }
    return resurrectees;
}

/**
 * Returns an array of cells generated by applying the rules of the game to a
 * group of cells, producing the next "generation" of cells.
 *
 * @param {Number[][]} cells
 * @returns {Number[][]}
 */
export function next(cells) {
    let newCells = [];
    const deadNeighbours = [];

    for (const cell of cells) {
        const liveNeighbours = [];

        for (const neighbour of neighbours(cell)) {
            if (contains(neighbour, cells)) {
                liveNeighbours.push(neighbour);
            } else {
                deadNeighbours.push(neighbour);
            }
        }

        if (liveNeighbours.length > 1) {
            newCells.push(cell);
        }
    }

    newCells = resurrectees(deadNeighbours).concat(newCells);

    return newCells;
}
