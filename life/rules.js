/**
 * Returns an array of cell co-ordinates immediately adjacent to the given cell.
 *
 * @param {String} stringCell
 * @returns {String[]}
 */
function neighbours(stringCell) {
    const [x, y] = destringify(stringCell);
    return [
        [x, y + 1],
        [x + 1, y + 1],
        [x + 1, y],
        [x + 1, y - 1],
        [x, y - 1],
        [x - 1, y - 1],
        [x - 1, y],
        [x - 1, y + 1]
    ].map((e) => e.toString());
}

/**
 * Turns a cell string into a cell array.
 *
 * @param {String} stringCell
 * @returns {Number[]}
 */
function destringify(stringCell) {
    return stringCell.split(',').map((e) => Number(e));
}

/**
 * Turns a set of arrays into a set of strings.
 *
 * @param {Set<Number[]>} cells
 * @returns {Set<String>}
 */
function stringifySet(cells) {
    return new Set(Array.from(cells).map((e) => e.toString()));
}

/**
 * Returns a boolean value indicating whether a given cell is present in a set
 * of cells.
 *
 * @param {String} stringCell
 * @param {Set<String>} stringCells
 * @returns Boolean
 */
function contains(cell, stringCells) {
    return stringCells.has(cell);
}

/**
 * Returns the set of cells which each occur exactly three times in the counter
 * object.
 *
 * @param {Object} counter
 * @returns {Set<Number[]>}
 */
function newCells(counter) {
    const result = new Set();
    for (const [cellString, count] of Object.entries(counter)) {
        if (count === 3) {
            const cell = cellString.split(',').map((e) => Number(e));
            result.add(cell);
        }
    }
    return new Set(result);
}

/**
 * Returns the set of cells generated by applying the rules of the game to an
 * initial set of cells, producing the next "generation" of cells.
 *
 * @param {Set<Number[]>} cells
 * @returns {Set<Number[]>}
 */
export function next(cells) {
    let result = new Set();
    const counter = {};

    for (const cell of cells) {
        const stringCell = cell.toString();
        const stringCells = stringifySet(cells);
        const liveNeighbours = new Set();

        for (const stringNeighbour of neighbours(stringCell)) {
            if (contains(stringNeighbour, stringCells)) {
                liveNeighbours.add(stringNeighbour);
            } else {
                counter[stringNeighbour] =
                    stringNeighbour in counter
                        ? counter[stringNeighbour] + 1
                        : 1;
            }
        }

        if (liveNeighbours.size > 1) {
            result.add(cell);
        }
    }

    for (const newCell of newCells(counter)) {
        result.add(newCell);
    }

    return result;
}
