/**
 * Returns an array of cell co-ordinates immediately adjacent to the given cell.
 *
 * @param {String} cell
 * @returns {String[]}
 */
function neighbours(cell) {
    const [x, y] = arrayFromString(cell);
    return [
        [x, y + 1],
        [x + 1, y + 1],
        [x + 1, y],
        [x + 1, y - 1],
        [x, y - 1],
        [x - 1, y - 1],
        [x - 1, y],
        [x - 1, y + 1]
    ].map((e) => e.toString());
}

/**
 * Turns a cell string into a cell array.
 *
 * @param {String} cell
 * @returns {Number[]}
 */
function arrayFromString(cell) {
    return cell.split(',').map((e) => Number(e));
}

/**
 * Returns the set of cells which each occur exactly three times in the counter
 * object.
 *
 * @param {Object} counter
 * @returns {Set<String>}
 */
function newCells(counter) {
    return new Set(Object.keys(counter).filter((cell) => counter[cell] === 3));
}

/**
 * Returns the set of cells generated by applying the rules of the game to an
 * initial set of cells, producing the next "generation" of cells.
 *
 * @param {Set<String>} cells
 * @returns {Set<String>}
 */
export function next(cells) {
    const result = new Set();
    const counter = {};

    for (const cell of cells) {
        let liveNeighbourCount = 0;

        for (const neighbour of neighbours(cell)) {
            if (cells.has(neighbour)) {
                liveNeighbourCount += 1;
            } else {
                counter[neighbour] =
                    neighbour in counter ? counter[neighbour] + 1 : 1;
            }
        }

        if (liveNeighbourCount > 1 && liveNeighbourCount < 4) {
            result.add(cell);
        }
    }

    for (const newStringCell of newCells(counter)) {
        result.add(newStringCell);
    }

    return result;
}
